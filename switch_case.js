var day = 43;
var dayName;

switch(day){
    case 1:
        dayName = "sunday";
        break;
    case 2:
        dayName = "monday";
        break;
    case 3:
        dayName = "tuesday";
        break;
    case 4:
        dayName = "wednusday";
        break;
    case 5:
        dayName = "thursday";
        break;
    case 6:
        dayName = "Friday";
        break;
    case 7:
        dayName = "saturday";
        break;
    default:
        dayName = "INVALID DAY VALUE";
        
}

console.log(dayName);